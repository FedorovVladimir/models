class Model {

    private int a;

    public Model() {
    }

    public Model(int a) {
        this.a = a;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    @java.lang.Override
    public java.lang.String toString() {
        return "Model{" +
                "a=" + a +
                '}';
    }
}
